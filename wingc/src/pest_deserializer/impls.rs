impl<'de> SeqAccess<'de> for CompositeDe<'de> {
    type Error = Void;
    fn next_element_seed<T>(&mut self, seed: T) -> Result<Option<T::Value>, Self::Error>
    where
        T: serde::de::DeserializeSeed<'de>,
    {
        if !self.de.has_next() {
            return Ok(None);
        }

        match &mut self.composite {
            Composite::Struct { fields } => {
                let (_, rest) = fields.split_first().unwrap();
                // *fields = rest;
                // if fst == "span" {
                //     let span = Span::from(self.de.peek().as_span());
                //     return seed.deserialize(span::span_de(span)).map(Some);
                // }
            }
            Composite::Tuple { len } => {
                *len -= 1;
            }
            Composite::Seq => (),
        }

        seed.deserialize(&mut self.de).map(Some)
    }
}

impl<'de> EnumAccess<'de> for &mut PestDeserializer<'de> {
    type Error = Void;

    type Variant = Self;

    fn variant_seed<V>(self, seed: V) -> Result<(V::Value, Self::Variant), Self::Error>
    where
        V: serde::de::DeserializeSeed<'de>,
    {
        let value = seed.deserialize(&mut *self)?;
        Ok((value, self))
    }
}

impl<'de> VariantAccess<'de> for &mut PestDeserializer<'de> {
    type Error = Void;

    fn unit_variant(self) -> Result<(), Self::Error> {
        todo!()
    }

    fn newtype_variant_seed<T>(self, seed: T) -> Result<T::Value, Self::Error>
    where
        T: serde::de::DeserializeSeed<'de>,
    {
        seed.deserialize(self)
    }

    fn tuple_variant<V>(self, len: usize, visitor: V) -> Result<V::Value, Self::Error>
    where
        V: Visitor<'de>,
    {
        self.deserialize_tuple(len, visitor)
    }

    fn struct_variant<V>(
        self,
        fields: &'static [&'static str],
        visitor: V,
    ) -> Result<V::Value, Self::Error>
    where
        V: Visitor<'de>,
    {
        self.deserialize_request(Composite::Struct { fields }, visitor)
    }
}
